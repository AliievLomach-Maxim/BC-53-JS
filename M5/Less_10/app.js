class Notes {
	static Priority = {
		LOW: 'low',
		NORMAL: 'normal',
		HIGH: 'high',
	}
	constructor(items) {
		this.items = items
	}
	addNote(note) {
		this.items.push(note)
	}
	removeNote(text) {
		this.items = this.items.filter((el) => el.text !== text)
	}
	updatePriority(text, newPriority) {
		this.items = this.items.map((item) => {
			if (item.text === text) {
				item.priority = newPriority
			}
			return item
		})
	}
}

